int	ft_isalnum(int c)
{
	int	res;

	res = 0;
	if ((c >= 'a' && c <= 'z')
		|| (c >= 'A' && c <= 'Z')
		|| (c >= '0' && c <= '9'))
		res = 1;
	return (res);
}
